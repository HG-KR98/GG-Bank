      const script = document.createElement("script");
      const API_KEY = ref("");

      axios
        .get("http://127.0.0.1:8000/findbank/")
        .then((response) => {
          API_KEY.value = response.data.API_KEY;
          script.src = `//dapi.kakao.com/v2/maps/sdk.js?appkey=${API_KEY.value}&autoload=false&libraries=services`; // Kakao Map API 스크립트 URL
          script.onload = () => window.kakao.maps.load(this.loadMap); // 스크립트가 로드되면 loadMap 함수 호출
          document.head.appendChild(script); // 스크립트를 문서의 head에 추가
        })
        .catch((error) => {
          console.log(error);
        });
    },
    // 지도를 로드하는 함수
    loadMap() {
      const container = document.getElementById("map"); // 지도를 표시할 div
      const options = {
        center: new window.kakao.maps.LatLng(33.450701, 126.570667), // 지도의 초기 중심 좌표
        level: 3, // 지도의 초기 확대 레벨
      };
      this.map = new window.kakao.maps.Map(container, options); // Kakao Map 객체 생성
      this.ps = new window.kakao.maps.services.Places(); // 장소 검색 객체 생성
    },

    // 1 장소를 검색하는 함수
    searchPlaces() {
      // 선택된 옵션들(시/도, 구, 동, 은행 이름)이 모두 존재하는지 확인
      if (
        !this.selectedCity ||
        !this.selectedDistrict ||
        !this.selectedSubdistrict ||
        !this.selectedBank
      ) {
        alert("모든 옵션을 선택하세요."); // 모든 옵션이 선택되지 않은 경우 경고 메시지 표시
        return;
      }

      // 이전의 마커와 인포윈도우 제거
      this.clearMarkersAndInfowindows();

      // 선택된 값들로 검색 쿼리 문자열 생성
      const query = `${this.selectedCity} ${this.selectedDistrict} ${this.selectedSubdistrict} ${this.selectedBank}`;

      // 카카오 지도 API를 사용하여 키워드 검색 수행
      this.ps.keywordSearch(query, (data, status, pagination) => {
        // 검색이 성공적으로 수행된 경우
        if (status === window.kakao.maps.services.Status.OK) {
          // 검색 결과 중 은행 이름을 포함하는 항목만 필터링하여 배열을 반환
          const filteredData = data.filter((place) =>
            place.place_name.includes(this.selectedBank)
          );
          // 필터링된 결과를 콜백 함수로 전달 -> 에러 표시를 위해 status도 함께 전달
          this.placesSearchCB(filteredData, status, pagination);
        } else {
          // 검색 중 에러가 발생한 경우 콘솔에 에러 메시지 출력
          console.error("Error searching places: ", status);
        }
      });
    },

    // 1-1 장소 검색 콜백 함수
    placesSearchCB(data, status, pagination) {
      if (status === window.kakao.maps.services.Status.OK) {
        const bounds = new window.kakao.maps.LatLngBounds();
        for (let i = 0; i < data.length; i++) {
          this.displayMarker(data[i]);
          bounds.extend(new window.kakao.maps.LatLng(data[i].y, data[i].x));
        }
        this.map.setBounds(bounds);
      } else {
        console.error("Error searching places: ", status);
      }
    },

    // 1-2 지도에 표시된 모든 마커와 인포윈도우를 제거하는 함수
    clearMarkersAndInfowindows() {
      // 모든 마커를 제거
      for (let i = 0; i < this.markers.length; i++) {
        this.markers[i].setMap(null);
      }
      this.markers = []; // 마커 배열 초기화

      // 모든 인포윈도우를 닫음
      for (let i = 0; i < this.infowindows.length; i++) {
        this.infowindows[i].close();
      }
      this.infowindows = []; // 인포윈도우 배열 초기화
    },

    // 1-3 지도에 마커를 표시하는 함수
    displayMarker(place) {
      const marker = new window.kakao.maps.Marker({
        map: this.map,
        position: new window.kakao.maps.LatLng(place.y, place.x),
      });

      // 마커 배열에 추가
      this.markers.push(marker);

      // 인포윈도우 생성
      const infowindow = new window.kakao.maps.InfoWindow({
        content: `<div style="padding:5px;font-size:12px;">${place.place_name}</div>`,
      });

      // 인포윈도우 배열에 추가
      this.infowindows.push(infowindow);

      // 마커에 클릭 이벤트를 등록
      window.kakao.maps.event.addListener(marker, "click", () => {
        // 마커를 클릭하면 장소명 인포윈도우에 표시
        infowindow.open(this.map, marker);
      });
    },

    // 2 현재 위치를 기반으로 주변 은행을 검색하는 함수
    searchNearbyBanks() {
      // 브라우저에서 Geolocation API를 사용할 수 있는지 확인
      if (navigator.geolocation) {
        // 현재 위치를 가져옴
        navigator.geolocation.getCurrentPosition(
          (position) => {
            // 위도와 경도를 변수에 저장
            const lat = position.coords.latitude;
            const lng = position.coords.longitude;
            // 현재 위치를 카카오 지도 객체로 생성
            this.currentLocation = new window.kakao.maps.LatLng(lat, lng);
            // 지도의 중심을 현재 위치로 설정
            this.map.setCenter(this.currentLocation);

            // 이전의 마커와 인포윈도우 제거
            this.clearMarkersAndInfowindows();

            // 카카오 지도 API의 장소 검색 객체 생성
            const places = new window.kakao.maps.services.Places();
            // '은행' 키워드로 장소 검색 수행
            places.keywordSearch(
              "은행",
              (data, status, pagination) => {
                // 검색이 성공적으로 수행된 경우
                if (status === window.kakao.maps.services.Status.OK) {
                  // 검색 결과 중 은행 이름을 포함하는 항목만 필터링하여 배열로 반환
                  const filteredData = data.filter((place) =>
                    place.place_name.includes(this.selectedBank)
                  );
                  // 필터링된 결과 배열을 콜백 함수로 전달
                  this.placesSearchCB(filteredData, status, pagination);
                } else {
                  // 검색 중 에러가 발생한 경우 콘솔에 에러 메시지 출력
                  console.error("Error searching places: ", status);
                }
              },
              {
                location: this.currentLocation, // 현재 위치를 기준으로 검색
                radius: 2000, // 2km 반경 내에서 검색
              }
            );
          },
          (error) => {
            // 위치 정보를 가져오는 중 에러가 발생한 경우
            console.error("Error occurred. Error code: " + error.code);
          }
        );
      } else {
        // 브라우저에서 Geolocation API를 사용할 수 없는 경우
        alert("Geolocation을 사용할 수 없습니다.");
      }
    },
  },
};
</script>

